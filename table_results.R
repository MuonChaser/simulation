## ---- format helpers ----
## "value(mc_se)" — both rounded to 1 decimal place
fmt_vs <- function(val, se, scale = 1) {
  v <- round(val * scale, 1)
  s <- round(se  * scale, 1)
  paste0(formatC(v, format = "f", digits = 1),
         "(", formatC(s, format = "f", digits = 1), ")")
}
## plain value
fmt_plain <- function(val, scale = 1) {
  formatC(round(val * scale, 1), format = "f", digits = 1)
}

## ---- load statistics for one label x one mechanism ----
load_stats <- function(label, mech_str) {
  csv_f <- file.path("results", paste0(label, "_stats.csv"))
  if (!file.exists(csv_f)) return(NULL)

  csv   <- read.csv(csv_f)
  row_s <- csv[csv$Method == "Strata" & csv$A == mech_str, ]
  row_g <- csv[csv$Method == "Group"  & csv$A == mech_str, ]

  make_entry <- function(row) {
    list(
      bias = fmt_vs   (row$Mean,      row$Mean_mcse, 1000),
      sd   = fmt_vs   (row$SD,        row$SD_mcse,   100),
      rmse = fmt_vs   (row$RMSE,      row$RMSE_mcse, 100),
      cp   = fmt_plain(row$CP,        1),              # already in %
      ci   = fmt_plain(row$CI_Length, 100)
    )
  }

  list(strat = make_entry(row_s),
       group = make_entry(row_g))
}

## ---- one data row ----
data_row <- function(param_str, show_param, st, method_label) {
  param_cell <- if (show_param) param_str else ""
  paste0(param_cell,
         " & ", method_label,
         " & ", st$bias,
         " & ", st$sd,
         " & ", st$rmse,
         " & ", st$cp,
         " & ", st$ci,
         " \\\\")
}

## ---- one scenario table ----
make_table <- function(sg) {
  lines <- c(
    "\\begin{table}[ht]",
    "\\centering",
    paste0("\\caption{Simulation results for Scenario~", sg$scenario_id,
           " (", sg$caption_desc, "; ", sg$fixed_desc, ").",
           " Bias~$(\\times 10^3)$, SD~$(\\times 10^2)$, RMSE~$(\\times 10^2)$:",
           " scaled statistics with Monte Carlo standard errors in parentheses.",
           " CP: empirical 95\\% coverage (\\%).",
           " CI: mean 95\\% confidence interval length $(\\times 10^2)$.}"),
    paste0("\\label{tab:simu", sg$scenario_id, "}"),
    "\\begin{tabular}{l l r r r r r}",
    "\\toprule",
    paste0(sg$param_name, " & Method",
           " & Bias $(\\times 10^3)$",
           " & SD $(\\times 10^2)$",
           " & RMSE $(\\times 10^2)$",
           " & CP (\\%)",
           " & CI $(\\times 10^2)$ \\\\"),
    "\\midrule"
  )

  for (mech in c("A=0", "A=1")) {
    a_val <- sub("A=", "", mech)
    lines <- c(lines,
      paste0("\\multicolumn{7}{l}{\\textit{Mechanism $a=", a_val, "$}} \\\\"),
      "\\midrule"
    )
    for (k in seq_along(sg$entries)) {
      e  <- sg$entries[[k]]
      st <- load_stats(e$label, mech)
      if (is.null(st)) next
      if (k > 1) lines <- c(lines, "\\addlinespace[3pt]")
      lines <- c(lines,
        data_row(e$param_str, TRUE,  st$strat, "Stratified (proposed)"),
        data_row(e$param_str, FALSE, st$group, "Unstratified (benchmark)")
      )
    }
    if (mech == "A=0") lines <- c(lines, "\\midrule")
  }

  c(lines, "\\bottomrule", "\\end{tabular}", "\\end{table}")
}

## ---- write output (Scenario 1 only) ----
all_lines <- c(
  "% simulation_table.tex -- auto-generated by table_results.R",
  "% Requires: \\usepackage{booktabs} in preamble",
  ""
)
all_lines <- c(all_lines, make_table(scenario_groups[[1]]), "")

writeLines(all_lines, "paper/simulation_table.tex")
cat("Saved: paper/simulation_table.tex  (tab:simu1)\n")

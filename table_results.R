## table_results.R
## Generates 1 LaTeX table (Scenario 1) in Liu & Yang (2020) style.
##
## Data source: {label}_stats.csv
##   Columns: Method, A, Mean, Mean_mcse, SD, SD_mcse, RMSE, RMSE_mcse,
##             CP, CP_mcse, CI_Length, CI_mcse
##
## Display format (Liu & Yang Table 1):
##   "X.X(X.X)"  for Bias (×1000), SD (×100), RMSE (×100) — value(MC_se)
##   "X.X"        for CP (%)
##   "XXX.X"      for CI length (×100)
## Output: paper/simulation_table.tex

